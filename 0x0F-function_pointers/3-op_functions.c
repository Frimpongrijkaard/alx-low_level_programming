#include  "3-calc.h"

/**
 * op_add- add two number
 * @a: first number
 * @b: second number
 *
 * Return: a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub- substract two numbers
 * @a: first number
 * @b: second number
 *
 * Return: a and b.
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul- multiply two numbers
 * @a: first number
 * @b: second number
 *
 * Return: a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div- division of two numbers
 * @a: first number
 * @b: second number
 *
 * Return: a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod- modulos of two number
 * @a: first number
 * @b: second number
 *
 * Return: a and b.
 */
int op_mod(int a, int b)
{
	return (a % b);
}

